    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        try
        {
            st=conn.createStatement();
            String UserID = ID.getText();
            String sql="SELECT * FROM STUDENT WHERE UserID ='"+UserID+"' ";
            
            rs= st.executeQuery(sql);
            if(rs.next())
            {
                name.setText(rs.getString("Name"));
                fatherName.setText(rs.getString("FatherName"));
                Blood.setText(rs.getString("BloodGroup"));
                city.setText(rs.getString("City"));
                Phone.setText(rs.getString("PhoneNumber"));
                cl.setText(rs.getString("Education"));
            }
            else
            {
              JOptionPane.showMessageDialog(null,"Record Not Found");  
            }
            
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_SearchActionPerformed

