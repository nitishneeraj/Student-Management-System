    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        try{
            st = conn.createStatement();
            String UserID=ID.getText();
            String Name=name.getText();
            String BloodGroup=(String)Blood.getText();
            String FatherName=fatherName.getText();
            String City=city.getText();
            String Education=cl.getText();
            String PhoneNumber=Phone.getText();

            String sql="UPDATE STUDENT SET UserID = '"+UserID+"', Name = '"+Name+"', BloodGroup = '"+BloodGroup+"', FatherName = '"+FatherName+"', City = '"+City+"', Education = '"+Education+"', PhoneNumber = '"+PhoneNumber+"' WHERE UserID = '"+UserID+"'";

            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Date is Successfully Update");
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_SubmitActionPerformed

